<p align="justify">It is a blissful opportunity for me to come back as a Director of NIT Delhi where my journey as a Director began. Apart from many important academic affairs, one of my visions and topmost priority is to know our alma mater more closely. Thus, to implement this thought; the institute have setup a new cell, i.e. the ‘<strong>Office of Alumni, International Affairs, and Public Relations</strong>’ which is committed to build the liaisoning between the Institute and our alma mater so as to identify the several ways through which the Institute can continue to offer the various opportunities for enhanced learning and personal development. I hope this cell will definitely prove to be a useful resource and networked hub for your career advancement and future progress. Also, this cell will act as a platform for you to come back to your old institute where you can meet with fellow-alumni to strengthen the social network. During the past years, I have been in constant touch with many of our esteemed Alumni and feel so proud to see them serving at such higher positions in various government organizations, corporate/ private sector, business firms along with various social and civic societies. I am very proud of our alumni graduates and aspire to have an illustrious list of such students and alumni who have not only strived to be the best in the world but to be the best for the world.</p>
